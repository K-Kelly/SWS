        public Team getTeamSearched(String s){
            for (Team a: teamList){
                if (a.getName().equalsIgnoreCase(s)){
                    a.sortPlayersToLists();
                    System.out.println(a.getAllPlayers().size());
                    return a;}
            }
            return new Team("Team Not Found", new ArrayList<Player>(), -1);
        }
